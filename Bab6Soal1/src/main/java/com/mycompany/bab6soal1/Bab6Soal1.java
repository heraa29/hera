package com.mycompany.bab6soal1;

/**
 *
 * @author Lenovo
 */
public class Bab6Soal1 {

    public static void main(String[] args) {
     // Menetapkan nilai secara langsung
        String nim = "1111";  // NIM mahasiswa
        String nama = "Agustav";  // Nama mahasiswa
        int nilai = 85;  // Nilai mahasiswa

        // Menentukan grade berdasarkan nilai
        char grade;
        if (nilai >= 85) {
            grade = 'A';
        } else if (nilai >= 70) {
            grade = 'B';
        } else if (nilai >= 55) {
            grade = 'C';
        } else if (nilai >= 40) {
            grade = 'D';
        } else {
            grade = 'E';
        }

        // Menampilkan hasil output
        System.out.println("### Pendataan Nilai Mahasiswa ###");
        System.out.println("NIM: " + nim);
        System.out.println("NAMA: " + nama);
        System.out.println("NILAI: " + nilai);
        System.out.println("Grade: " + grade);
    }
}