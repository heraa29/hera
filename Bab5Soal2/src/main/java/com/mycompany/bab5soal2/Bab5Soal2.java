public class Bab5Soal2 {

   public static void main(String[] args) {
      // Menetapkan nilai alas dan tinggi secara langsung
        double alas = 17;  // Misalnya, nilai alas adalah 17
        double tinggi = 11;  // Misalnya, nilai tinggi adalah 11

        // Menghitung luas segitiga
        double luas = (alas * tinggi) / 2;

        // Menampilkan hasil perhitungan
        System.out.println("Luas Segitiga dengan alas " + alas + " dan tinggi " + tinggi + " adalah: " + luas);
    }
}