package com.mycompany.bab4soal2;

/**
 *
 * @author Lenovo
 */
public class Bab4Soal2 {

    public static void main(String[] args) {
         // Deklarasi variabel
        int w, x, y, z;
        x = 6;
        w = 5;

        // Operasi aritmatika dengan inkrementasi
        y = 8 - x++;
        z = 8 - ++w;

        // Menampilkan nilai variabel
        System.out.println("Nilai w: " + w); // Output: Nilai w: 6 (nilai setelah inkrementasi)
        System.out.println("Nilai x: " + x); // Output: Nilai x: 7 (nilai setelah inkrementasi)
        System.out.println("Nilai y: " + y); // Output: Nilai y: 2 (hasil dari operasi sebelum inkrementasi x)
        System.out.println("Nilai z: " + z); // Output: Nilai z: 2 (hasil dari operasi setelah inkrementasi w)
    }
    }
